public class Unary {
    public static void main(String[] args) {
        // Unary Operator
        // ++, --, !
        int a = 10;
        int b = a++; // a = 11, b = 10

        // int b = ++a; // a = 11, b = 11
        System.out.println("a is : " + a);
        System.out.println("b is : " + b);
    }
}
