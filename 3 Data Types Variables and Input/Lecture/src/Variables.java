public class Variables {
    public static void main(String[] args) {
        // This is called initialisation
         int myNum = 1969;

        // This is called declaration
        int yourNum = 0;
        System.out.println(yourNum);
        yourNum = 2111;
        System.out.println(yourNum);
        yourNum = 2004;
        System.out.println(yourNum);


        // If we didn't add f at the end it will assume the variable as double
        float myFloat = 5.9f;
        double myDouble = 3.14159;
        System.out.println(myFloat);
        System.out.println(myDouble);


        boolean isVegetarian = false;
        System.out.println(isVegetarian);


        String wishes = "Good Morning\n:)";
        System.out.println(wishes);


        char myCharacter = 'K';
        System.out.println(myCharacter);
    }
}